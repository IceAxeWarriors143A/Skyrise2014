// One joystick tank drive control. Also called arcade drive
void tank_drive(float x, float y)
{
	if(abs(x) > 5 || abs(y) > 5)
	{
		motor[right] = (x - y) / 2;
		motor[left] = (x + y) / 2;
	}
}

// Lift for the claw arm
void drive_rack_lift(int speed)
{
	if(abs(speed) > 5)
	{
		motor[rightRackLift] = speed;
		motor[leftRackLift] = -speed;
	}
}

// Drive the lift down
void rack_lift_down()
{
	drive_rack_lift(-127);
}

// Drive the lift up
void rack_lift_up()
{
	drive_rack_lift(127);
}

// Stop the lift
void rack_lift_stop()
{
	drive_rack_lift(0);
}

